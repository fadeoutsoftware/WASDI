package wasdi.shared.viewmodels.monitoring;

public class Frequency {

	private Long hz;
	private Integer mhz;
	private Double ghz;
	
	public Long getHz() {
		return hz;
	}
	public void setHz(Long hz) {
		this.hz = hz;
	}
	public Integer getMhz() {
		return mhz;
	}
	public void setMhz(Integer mhz) {
		this.mhz = mhz;
	}
	public Double getGhz() {
		return ghz;
	}
	public void setGhz(Double ghz) {
		this.ghz = ghz;
	}

}